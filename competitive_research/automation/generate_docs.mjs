async function readJson(file) {
  return JSON.parse(await readFile(file, 'utf8'));
}

async function maybeRead(dir, name) {
  try { return await readJson(path.join(dir, name)); } catch { return null; }
}

function img(p) {
  return p ? `![截图](../${p})` : '';
}

function table(rows, headers = ['页面/能力', '关键内容', '产品设计要点']) {
  const esc = (s) => String(s ?? '').replace(/\|/g, '\\|').replace(/\n/g, '<br>');
  return [
    `| ${headers.map(esc).join(' | ')} |`,
    `| ${headers.map(() => '---').join(' | ')} |`,
    ...rows.map(r => `| ${r.map(esc).join(' | ')} |`),
  ].join('\n');
}

function bullets(items) {
  return items.filter(Boolean).map(i => `- ${i}`).join('\n');
}

function controls(page, max = 20) {
  return (page?.controls || []).map(x => x.text || x.placeholder).filter(Boolean).filter((x, i, arr) => arr.indexOf(x) === i).slice(0, max);
}

function headers(page) {
  const vals = [];
  for (const t of page?.tables || []) {
    if (t.headers?.length) vals.push(t.headers.join('、'));
  }
  return [...new Set(vals)];
}

function bodyLine(page, len = 260) {
  return String(page?.bodyText || '').replace(/\s+/g, ' ').slice(0, len);
}

